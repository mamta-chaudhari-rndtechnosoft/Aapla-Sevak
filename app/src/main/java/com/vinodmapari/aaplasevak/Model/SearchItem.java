package com.vinodmapari.aaplasevak.Model;

public class SearchItem {

    private String id;
    private String name;
    private String middle_name;
    private String surname;
    private String fullname;
    private String voter_id;
    private String booth_no;
    private String s_no;
    private String voting_center;
    private String gender;

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMiddle_name() {
        return middle_name;
    }

    public void setMiddle_name(String middle_name) {
        this.middle_name = middle_name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getVoter_id() {
        return voter_id;
    }

    public void setVoter_id(String voter_id) {
        this.voter_id = voter_id;
    }

    public String getBooth_no() {
        return booth_no;
    }

    public void setBooth_no(String booth_no) {
        this.booth_no = booth_no;
    }

    public String gets_no() {
        return s_no;
    }

    public void sets_no(String s_no) {
        this.s_no = s_no;
    }

    public String getVoting_center() {
        return voting_center;
    }

    public void setVoting_center(String voting_center) {
        this.voting_center = voting_center;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

}
